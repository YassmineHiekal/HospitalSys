    public boolean getuserlist(String ID,String FName, String LName)
{
       Connection connection = getConnection();
       int id = Integer.valueOf(ID);
       String query = "select Fname , Lname from patient where ID ="+id;
       Statement st;
       ResultSet rs;
       boolean flag=false;
       String Firstname ;
       String Lastname ;
        try {
           st = connection.createStatement();
           rs = st.executeQuery(query);
           PatientSignIn patient;
           while(rs.next())
           {
           patient = new PatientSignIn(rs.getString("Fname"),rs.getString("Lname"));
           Firstname = patient.GetFname();
           Lastname = patient.GetLname();
           if (FName.equals(Firstname) && LName.equals(Lastname))
           {
               flag = true;
           }
                      
       } }
      catch (SQLException e) {
          System.out.println (e);
       }
    return flag;
}


    
